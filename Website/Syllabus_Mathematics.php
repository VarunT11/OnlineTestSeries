<d>Algebra</d>
<br>
<ti>Algebra of complex numbers, addition, multiplication, conjugation, polar representation, properties of modulus and principal argument, triangle inequality, cube roots of unity, geometric interpretations.
<br>Quadratic equations with real coefficients, relations between roots and coefficients, formation of quadratic equations with given roots, symmetric functions of roots.
<br>Arithmetic, geometric and harmonic progressions, arithmetic, geometric  and harmonic means, sums of finite arithmetic and geometric progressions, infinite geometric series, sums of squares and cubes of the first n natural numbers.
<br>Logarithms and their properties.
<br>Permutations and combinations, binomial theorem for a positive integral index, properties of binomial coefficients.
<br>Matrices as a rectangular array of real numbers, equality of matrices, addition, multiplication by a scalar and product of matrices, transpose of a matrix, determinant of a square matrix of order up to three, inverse of a square matrix of order up to three, properties of these matrix operations, diagonal, symmetric and skew-symmetric matrices and their properties, solutions of simultaneous linear equations in two or three variables.
<br>Addition and multiplication rules of probability, conditional probability, Bayes Theorem, independence of events, computation of probability of events using permutations and combinations.
</ti>
<br>
<d>Trigonometry</d>
<br>
<ti>Trigonometric functions, their periodicity and graphs, addition and subtraction formulae, formulae involving multiple and sub-multiple angles, general solution of trigonometric equations.
<br>Relations between sides and angles of a triangle, sine rule, cosine rule, half-angle formula and the area of a triangle, inverse trigonometric functions (principal value only). 
</ti>
<br>
<d>Analytical geometry</d>
<br>
<ti>Two dimensions: Cartesian coordinates, distance between two points, section formulae, shift of origin.
<br>Equation of a straight line in various forms, angle between two lines, distance of a point from a line; Lines through the point of intersection of two given lines, equation of the bisector of the angle between two lines, concurrency of lines;  Centroid, orthocentre, incentre and circumcentre of a triangle.
<br>Equation of a circle in various forms, equations of tangent, normal and chord.
<br>Parametric equations of a circle, intersection of a circle with a straight line or a circle, equation of a circle through the points  of  intersection of two circles and those of a circle and a straight line.
<br>Equations of a parabola, ellipse and hyperbola in standard form, their foci, directrices and eccentricity, parametric equations, equations of tangent and normal. 
<br>Locus problems.
<br>Three dimensions: Direction cosines and direction ratios, equation of a straight line in space, equation of a plane, distance of a point from a plane.
</ti>
<br>
<d>Differential calculus</d>
<br>
<ti>Real valued functions of a real variable, into, onto and one-to-one functions, sum, difference, product and quotient of two functions, composite functions, absolute value, polynomial, rational, trigonometric, exponential and logarithmic functions.
<br>Limit and continuity of a function, limit and continuity of the sum, difference, product and quotient of two functions, L’Hospital rule of evaluation of limits of functions.
<br>Even and odd functions, inverse of a function, continuity of composite functions, intermediate value property of continuous functions.
<br>Derivative of a function, derivative of the sum, difference, product and quotient of two functions, chain rule, derivatives of polynomial, rational, trigonometric, inverse trigonometric, exponential and logarithmic functions.
<br>Derivatives of implicit functions, derivatives up to order two, geometrical interpretation of the derivative, tangents and normals, increasing and decreasing functions, maximum and minimum values of a function, Rolle’s theorem and Lagrange’s mean value theorem.
</ti>
<br>
<d>Integral calculus</d>
<br>
<ti>Integration as the inverse process of differentiation, indefinite integrals of standard functions, definite integrals and their properties, fundamental theorem of integral calculus.
<br>Integration by parts, integration by the methods of substitution and partial fractions, application of definite integrals to the determination of areas involving simple curves.
<br>Formation of ordinary differential equations, solution of homogeneous differential equations, separation of variables method, linear first order differential equations.
</ti>
<br>
<d>Vectors</d>
<br>
<ti>Addition of vectors, scalar multiplication, dot and cross products, scalar triple products and their geometrical interpretations.</ti>
